/* Example for ceil() and floor() functions: */

#include<stdio.h>
#include<math.h>

int main()
{
    printf("\n ceil(1.44) : %f" , ceil(1.44) );
    printf("\n ceil(1.66) : %f" , ceil(1.66) );
 
    printf("\n floor(1.44) : %f" , floor(1.44) );    
    printf("\n floor(1.66) : %f" , floor(1.66) );

    return 0;
}
